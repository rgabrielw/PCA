#include <stdio.h>

int main() 
{
    float SH, horas, salBruto, salLiquido = 0;
    
    printf("Insira o quanto vc ganha por hora: ");
    scanf("%f",& SH);
    
    printf("Insira o número de horas que vc trabalhou no mês: ");
    scanf("%f",& horas);
    
    salBruto = horas * SH;
    
    printf("\nO seu salário bruto foi de: R$%.2f", salBruto);
    
    salLiquido = salBruto - (salBruto*0.11) - (salBruto*0.08) - (salBruto*0.05);
    
    printf("\nO seu salário líquido foi de: R$%.2f", salLiquido);
    
    return 0;
}
